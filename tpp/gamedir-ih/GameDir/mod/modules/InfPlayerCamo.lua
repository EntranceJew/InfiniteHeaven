  if setting+1>#this.names then
    
    setting=0
  end
  if not IHH then
  else
    if setting==0 then
    --IhkCharacter.SetOverrideCharacterSystem(false)
    else
      IhkCharacter.SetOverrideCharacterSystem(true)--DEBUGNOW
