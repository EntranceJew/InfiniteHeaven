function this.OnAllocateQuestFova(n)
  local faces={}
  local bodies={}
  local l=false
  local d=false
  local s=false
  local o=false
  mvars.ene_questArmorId=0
  mvars.ene_questBalaclavaId=0
  if n.isQuestBalaclava==true then
    local e={}
    if TppLocation.IsAfghan()then
      mvars.ene_questBalaclavaId=TppDefine.QUEST_FACE_ID_LIST.AFGH_BALACLAVA
    elseif TppLocation.IsMiddleAfrica()then
      mvars.ene_questBalaclavaId=TppDefine.QUEST_FACE_ID_LIST.MAFR_BALACLAVA
    end
    mvars.ene_questGetLoadedFaceTable=TppSoldierFace.GetLoadedFaceTable{}
    if mvars.ene_questGetLoadedFaceTable~=nil then
      local n=#mvars.ene_questGetLoadedFaceTable
      if mvars.ene_questBalaclavaId~=0 and n>0 then
        e={mvars.ene_questBalaclavaId,TppDefine.QUEST_ENEMY_MAX,0}
        table.insert(faces,e)d=true
      end
    end
  end
  if n.isQuestArmor==true then
    local e={}
    if TppLocation.IsAfghan()then
      mvars.ene_questArmorId=TppDefine.QUEST_BODY_ID_LIST.AFGH_ARMOR
    elseif TppLocation.IsMiddleAfrica()then
      if n.soldierSubType=="PF_A"then
        mvars.ene_questArmorId=TppDefine.QUEST_BODY_ID_LIST.MAFR_ARMOR_CFA
      elseif n.soldierSubType=="PF_B"then
        mvars.ene_questArmorId=TppDefine.QUEST_BODY_ID_LIST.MAFR_ARMOR_ZRS
      elseif n.soldierSubType=="PF_C"then
        mvars.ene_questArmorId=TppDefine.QUEST_BODY_ID_LIST.MAFR_ARMOR_RC
      end
    end
    if mvars.ene_questArmorId~=0 then
      e={mvars.ene_questArmorId,TppDefine.QUEST_ENEMY_MAX,0}
      table.insert(bodies,e)l=true
    end
  end
  if(n.enemyList and Tpp.IsTypeTable(n.enemyList))and next(n.enemyList)then
    for n,e in pairs(n.enemyList)do
      if e.enemyName then
        if e.bodyId then
          local n=1
          local e={e.bodyId,n,0}table.insert(bodies,e)l=true
        end
        if e.faceId then
          local n=1
          local e={e.faceId,n,0}table.insert(faces,e)d=true
        end
      end
    end
  end
  if(n.hostageList and Tpp.IsTypeTable(n.hostageList))and next(n.hostageList)then
    for n,e in pairs(n.hostageList)do
      if e.hostageName then
        if e.bodyId then
          local n=1
          local e={e.bodyId,0,n}table.insert(bodies,e)s=true
        end
        if e.faceId then
          local n=1
          local e={e.faceId,0,n}table.insert(faces,e)o=true
        end
        if e.isFaceRandom then
          local e=TppQuest.GetRandomFaceId()
          if e then
            local n=1
            local e={e,0,n}table.insert(faces,e)o=true
          end
        end
      end
    end
  end
  if s==true then
    local a={}
    local n=false
    for t,e in ipairs(bodies)do
      if e[3]>=1 then
        local e=e[1]if p(e)then
          table.insert(a,e)n=true
        end
      end
    end
    if n==true then
      TppSoldierFace.SetBodyFovaUserType{hostage=hostageBodyTable}
    end
  end
  local i="SetNone"if((l==true or d==true)or s==true)or o==true then
    local n=l or s
    local e=d or o
    if n==true and e==true then
      TppSoldierFace.SetAndConvertExtendFova{face=faces,body=bodies}i="SetFaceAndBody"
    elseif e==true then
      TppSoldierFace.SetAndConvertExtendFova{face=faces}i="SetFace"
    elseif n==true then
      TppSoldierFace.SetAndConvertExtendFova{body=bodies}i="SetBody"
    end
  end
  local r
  if l==true or d==true then
    if i=="SetFaceAndBody"then
      r={id="InitializeAndAllocateExtendFova",face=faces,body=bodies}
    elseif i=="SetFace"then
      r={id="InitializeAndAllocateExtendFova",face=faces}
    elseif i=="SetBody"then
      r={id="InitializeAndAllocateExtendFova",body=bodies}
    end
    if r then
      GameObject.SendCommand({type="TppSoldier2"},r)GameObject.SendCommand({type="TppCorpse"},r)
    end
  end
  if s==true or o==true then
    if i=="SetFaceAndBody"then
      TppSoldierFace.ReserveExtendFovaForHostage{face=faces,body=bodies}
    elseif i=="SetFace"then
      TppSoldierFace.ReserveExtendFovaForHostage{face=faces}
    elseif i=="SetBody"then
      TppSoldierFace.ReserveExtendFovaForHostage{body=bodies}
    end
  end
  local n=n.heliList
  if(n and Tpp.IsTypeTable(n))and next(n)then
    this.LoadQuestHeli(n[1].coloringType)
  end
end


